    $(function() {
        $('.header,.footer').hide();
        /*初始化可视区图片*/
        var minWid = 1700;
        function init(){
            $('.panel,.container,.container>img').css({'height': $(window).height()});
        }
        init();
        var i = 0;
        var arrImgWid = [],
            arrImgHei = [],
            arrConWid = [],
            arrConHei = [];

        var imgLen = $('.zt-con img').length;
        var contentLen = $('.content').length;
        for(i=0;i<imgLen;i++){
            arrImgWid.push($('.zt-con img').eq(i).width());
            arrImgHei.push($('.zt-con img').eq(i).height());
        }
        for(i=0;i<contentLen;i++){
            arrConWid.push($('.content').eq(i).width());
            arrConHei.push($('.content').eq(i).height());
        }
        if($(window).width()<minWid){
            var oScale = $(window).width()/minWid;
            $('.container>img').css({'left':-(minWid-$(window).width())/2})
            for(i=0;i<imgLen;i++){
                $('.zt-con img').eq(i).css({'width':(arrImgWid[i]*oScale),'height':(arrImgHei[i]*oScale)});
                $('.zt-con img').eq(i).parent('a').css({'width':(arrImgWid[i]*oScale),'height':(arrImgHei[i]*oScale)});
            }
            for(i=0;i<imgLen;i++){
                $('.content').eq(i).css({'width':(arrConWid[i]*oScale),'height':(arrConHei[i]*oScale*1.1)})
            }

        }

        /*初始化滚动页面*/
        $.scrollify({
            section:".panel",
            scrollbars:false
        });

        /*右侧导航点击事件*/
        var panelLen = $('.panel').length;
        var tar = '';
        for(i=0;i<=panelLen;i++){
            $('.sub-nav-'+i).click(function(e) {
                e.preventDefault();
                tar = '#part'+($(this).parent('li').index()+1);
                $.scrollify("move",tar);
            });
        }


        /*轮播效果*/
        var oUlWid = $('.warm-03 ul').width();
        $('.warm-03 ul li:eq(1)').css({'left':oUlWid+30});
        $('.warm-04 ul li:eq(1)').css({'left':oUlWid+30});
        $('.warm-05 ul li:eq(1)').css({'left':oUlWid+30});

        var index3 = 0,
            index4 = 0,
            index5 = 0;

        function moveLeft(obj,index){
            var oLi1 = obj.siblings('ul.slides').find('li:eq(0)');
            var oLi2 = obj.siblings('ul.slides').find('li:eq(1)');
            if (index==0) {
                oLi1.stop(true,false).animate({'left':-oUlWid+30},500);
                oLi2.stop(true,false).animate({'left':0},500);
            }
            else{
                if(index%2==1){
                    oLi1.css({'left':oUlWid});
                    oLi1.stop(true,false).animate({'left':0},500);
                    oLi2.stop(true,false).animate({'left':-oUlWid-30},500);
                }
                else{
                    oLi2.css({'left':oUlWid});
                    oLi2.stop(true,false).animate({'left':0},500);
                    oLi1.stop(true,false).animate({'left':-oUlWid-30},500);
                }
            }
        }
        function moveRight(obj,index){
            var oLi1 = obj.siblings('ul.slides').find('li:eq(0)');
            var oLi2 = obj.siblings('ul.slides').find('li:eq(1)');
            if(index%2==1){
                oLi1.css({'left':-oUlWid});
                oLi1.stop(true,false).animate({'left':0},500);
                oLi2.stop(true,false).animate({'left':oUlWid+30},500);
            }
            else{
                oLi2.css({'left':-oUlWid});
                oLi2.stop(true,false).animate({'left':0},500);
                oLi1.stop(true,false).animate({'left':oUlWid+30},500);
            }
        }

        $('.warm-03 .arrow-left').click(function(){
            moveLeft($(this),index3);
            index3++;
        })
        $('.warm-03 .arrow-right').click(function(){
            moveRight($(this),index3);
            index3++;
        })
        $('.warm-04 .arrow-left').click(function(){
            moveLeft($(this),index4);
            index4++;
        })
        $('.warm-04 .arrow-right').click(function(){
            moveRight($(this),index4);
            index4++;
        })
        $('.warm-05 .arrow-left').click(function(){
            moveLeft($(this),index5);
            index5++;
        })
        $('.warm-05 .arrow-right').click(function(){
            moveRight($(this),index5);
            index5++;
        })

