<script type="text/javascript">
    $(function(){
        var scrollTop = 0,i = 0,j = 0;
        var infoLen = $('.info').length;
        var onOff1 = true,onOff2 = true;

        function onFadeIn(){
            if(i<infoLen){
                $('.info').eq(i).fadeIn(500,function(){
                    i++;
                    onFadeIn();
                });
            }
            else{
                onOff1 = !onOff1;
            }
        }

        $(window).scroll(function(){
            scrollTop = $(document).scrollTop();
            if(scrollTop>=1000){
                if(onOff1){
                    onOff1 = !onOff1;
                    onFadeIn();
                }
            }
            else{
                i=0;
                $('.info').hide();
            }
            if(scrollTop>=3900){
                if(onOff2){
                    onOff2 = !onOff2;
                    $('.tea').slideDown(2000,function(){
                        onOff2 = !onOff2;
                    })
                }
            }
            else{
                $('.tea').hide();
            }
        })
    })
</script>