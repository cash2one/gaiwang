$(function(){
	var i = 0;
	var len = $('.car-washing-10 a').length;
	
	var onOff = true;
	
	function scaling(index){
		for(i=0;i<len;i++){
			if(i==index)
				continue;
			var oImg = $('.car-washing-10 a').eq(i).find('img');
			var oWid = oImg.width();
			var oHei = oImg.height();
			var oLeft = parseInt($('.car-washing-10 a').eq(i).css('left'));
			var oTop = parseInt($('.car-washing-10 a').eq(i).css('top'));
			if(onOff){
				if(oWid<20 || oHei<29){
					oImg.css({
						'width':oWid+1,
						'height':oHei+1
					});
					$('.car-washing-10 a').eq(i).css({
						'left':oLeft-1,
						'top':oTop-1
					})
				}
				else{
					onOff=!onOff;
				}
			}
			else{
				if(oWid>15 || oHei>24){
					oImg.css({
						'width':oWid-1,
						'height':oHei-1
					});
					$('.car-washing-10 a').eq(i).css({
						'left':oLeft+1,
						'top':oTop+1
					})
				}
				else{
					onOff=!onOff;
				}
			}
		}	
	}
	timer = setInterval(function(){
		scaling(7);
	},500);

	var _index = 7;
	$('.car-washing-10 a').mouseover(function(){
		clearInterval(timer)
		/*timer = setInterval(function(){
			scaling($(this).index());
		},500)*/
		$('.car-washing-10 a').removeAttr('style');
		$('.car-washing-10 a').find('img').removeAttr('style');
		if(_index!=-1){
			$(this).parent().children().eq(_index).removeClass('a'+(_index+1)+'-active');
			$(this).parent().children().eq(_index).find('img').attr('src','<?php echo ATTR_DOMAIN;?>/zt/car-washing/coordinate.png');
			$(this).parent().children().eq(_index).find('img').css({
				'width':15,
				'height':24	
			})
		}
		_index = $(this).index();

		$(this).find('img').attr('src','<?php echo ATTR_DOMAIN;?>/zt/car-washing/a'+($(this).index()+1)+'-active.png');
		$(this).find('img').css({
			'width':48,
			'height':70
		});
		$(this).addClass('a'+($(this).index()+1)+'-active');
		//底部介绍内容
		$('.car-washing-11 a').hide();
		$('.car-washing-11 a').eq($(this).index()).show();
	})
	$('.car-washing-10 a').eq(7).find('img').attr('src','<?php echo ATTR_DOMAIN;?>/zt/car-washing/a8-active.png');
